/**
  * @brief  Function implementing the receiver thread.
  * @param  argument: Not used 
  * @retval None
  */
/* USER CODE END Header_receiverTask */
void receiverTask(void const * argument)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for(;;)
  {
	  BaseType_t status;
	  uint8_t buffer, bothPWM, options, checksum;
	  BaseType_t mode = 1;
	  do {
		  status = xQueueReceive(uart3_rx_queueHandle, &buffer, 1000 );
	  } while ( !status || buffer!=255);
	  status = xQueueReceive(uart3_rx_queueHandle, &bothPWM, 5 / portTICK_PERIOD_MS );
	  if (status) {
		  status = xQueueReceive(uart3_rx_queueHandle, &options, 5 / portTICK_PERIOD_MS );
		  if (status) {
			  status = xQueueReceive(uart3_rx_queueHandle, &checksum, 5 / portTICK_PERIOD_MS );
			  if ( status && ( (255 ^ bothPWM ^ options ^ checksum) ==0) )
			  {
				  struct setPWMmessage_t msg;
				  if (options & 0b00000010) mode=1;
				  if (options & 0b00000001) mode=0;
				  if (mode) {
					  msg.pwmRED = (bothPWM & 0x0F * 100) / 10; // or just *10
					  msg.pwmGRN = ( (bothPWM>>4) & 0x0F * 100) / 10; //
				  } else {
					  msg.pwmRED = 0;
					  msg.pwmGRN = 0;
				  }
				  xQueueSend(pwm_queueHandle, &msg, 5 / portTICK_PERIOD_MS); // if QW is full then sth. is wrong
			  }
		  }
	  }

    osDelay(1);
  }
  /* USER CODE END 5 */ 
}

