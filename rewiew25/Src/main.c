/**
* @brief Function implementing the setPWM thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_setPWMTask */
void setPWMTask(void const * argument)
{
  /* USER CODE BEGIN setPWMTask */
	struct setPWMmessage_t msg;
  /* Infinite loop */
  for(;;)
  {
	BaseType_t status = xQueueReceive(pwm_queueHandle, &msg, 200 / portTICK_PERIOD_MS );
	if (status==pdTRUE) {
	  TIM3->CCR3 = msg.pwmRED; // range: 0 to 100
	  TIM8->CCR2 = msg.pwmGRN; // range: 0 to 100
	} else {
	  TIM3->CCR3 = 0; // range: 0 to 100
	  TIM8->CCR2 = 0; // range: 0 to 100
	}
    osDelay(1);
  }
  /* USER CODE END setPWMTask */
}

